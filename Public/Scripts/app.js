"use strict";

console.log('app.js is running.');

// JSX

var app = {
    title: "Indecision",
    subtitle: "Make tricky decisions with the help of ReactJS!"
};
var user = {
    name: "Joe Schmoe",
    age: 31,
    location: 'Yew Nork City'
};

var getUserLocation = function getUserLocation(location) {
    return location != null && location != '' ? React.createElement(
        "p",
        null,
        "Location: ",
        location
    ) : null;
};

var template = React.createElement(
    "div",
    null,
    React.createElement(
        "h1",
        { className: "head", id: "jsx-h1" },
        app.title
    ),
    React.createElement("br", null),
    React.createElement(
        "span",
        null,
        app.subtitle
    ),
    React.createElement(
        "ol",
        null,
        React.createElement(
            "li",
            null,
            " One Thing "
        ),
        React.createElement(
            "li",
            null,
            " Two Thing "
        ),
        React.createElement(
            "li",
            null,
            " Red Thing "
        ),
        React.createElement(
            "li",
            null,
            " Blu Thing "
        )
    )
);

var userInfo = React.createElement(
    "div",
    null,
    React.createElement(
        "h1",
        null,
        user.name
    ),
    React.createElement(
        "p",
        null,
        "Age:      ",
        user.age
    ),
    getUserLocation(user.location)
);

